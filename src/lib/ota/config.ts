export function getOTAById(id: string): OTAConfig | undefined {
  return ALL_OTAS.find(ota => ota.id === id);
}

export function getOTAsByRegion(region: string): OTAConfig[] {
  return ALL_OTAS.filter(ota => ota.region === region);
}

export function getOTAsByType(type: string): OTAConfig[] {
  return ALL_OTAS.filter(ota => ota.type === type);
}

export function getOTAsByPriority(priority: string): OTAConfig[] {
  return ALL_OTAS.filter(ota => ota.priority === priority);
}

export function getOTAsWithFeature(feature: OTAFeature): OTAConfig[] {
  return ALL_OTAS.filter(ota => ota.features.includes(feature));
}

export function getOTACount(): { total: number; byRegion: Record<string, number>; byPriority: Record<string, number> } {
  return {
    total: ALL_OTAS.length,
    byRegion: {
      global: GLOBAL_OTAS.length,
      india: INDIAN_OTAS.length,
      asia_pacific: ASIA_PACIFIC_OTAS.length,
      europe: EUROPEAN_OTAS.length,
      middle_east: MIDDLE_EAST_AFRICA_OTAS.length,
      vacation_rental: VACATION_RENTAL_OTAS.length,
      metasearch: METASEARCH_OTAS.length,
    },
    byPriority: {
      critical: ALL_OTAS.filter(ota => ota.priority === 'critical').length,
      high: ALL_OTAS.filter(ota => ota.priority === 'high').length,
      medium: ALL_OTAS.filter(ota => ota.priority === 'medium').length,
      low: ALL_OTAS.filter(ota => ota.priority === 'low').length,
    },
  };
}
